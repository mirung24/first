package vo.shopvo;

public class SInvenVO {
	String compcd;
	Object productid, stock, appropriatestock;
	
	public String getCompcd() {
		return compcd;
	}
	public void setCompcd(String compcd) {
		this.compcd = compcd;
	}
	public Object getProductid() {
		return productid;
	}
	public void setProductid(Object productid) {
		this.productid = productid;
	}
	public Object getStock() {
		return stock;
	}
	public void setStock(Object stock) {
		this.stock = stock;
	}
	public Object getAppropriatestock() {
		return appropriatestock;
	}
	public void setAppropriatestock(Object appropriatestock) {
		this.appropriatestock = appropriatestock;
	}
}
